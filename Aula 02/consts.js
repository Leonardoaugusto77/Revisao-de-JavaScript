// Const no JavaScript?

var canal = "Project_Heisen" // O Armazenamento com o Var permite que você altere o valor dela a qualquer momento no código (Lembrando que o escopo dela é global).  

canal = "Revisão de JavaScript"

console.log(canal)


// O que são Constantes ?

const canal = "Project_Heisen"    // São Variável que não podem ser alteradas

canal = "Revisão de JavaScript" // Nessa caso não será possível alterar o valor da Variável já que ela é uma Const

console.log(canal)


