// O que é o comando Do While?


/*
    Do While executa o bloco de comando mesmo se a condição passada for false

    Exemplo:

    let num = 10

    while(num<5){
        console.log("Leonardo Augusto - Desenvolvedor Front - end jr")
        num++
    }

    No caso a cima já que o teste deu false o while não será executado

    Vamos ver como o Do While

 */

    let num = 10

    do{ // Faça 
        console.log("Leonardo Augusto - Desenvolvedor Front - end jr")
    } while(num < 5) // teste

    // No caso a cima primeiro e executado o bloco de comando e depois e feito o teste, isso garante a execução pelo menos uma vez do bloco




